"use client";
import { useState } from "react";
import { signIn } from "next-auth/react";
import styles from "./GoogleButton.module.scss";
import type { GoogleButtonProps } from "./GoogleButton.types";

export default function GoogleButton({
  label = "Continue with Google",
  callbackUrl,
  className,
}: GoogleButtonProps) {
  const [loading, setLoading] = useState(false);

  async function onClick() {
    setLoading(true);
    try {
      const target =
        callbackUrl || (typeof window !== "undefined" ? window.location.origin : "/");
      await signIn("google", { callbackUrl: target });
    } finally {
      setLoading(false);
    }
  }

  return (
    <button
      onClick={onClick}
      disabled={loading}
      aria-busy={loading}
      className={[styles.btn, className].filter(Boolean).join(" ")}
    >
      {/* Google icon */}
      <svg className={styles.icon} viewBox="0 0 48 48" aria-hidden>
        <path fill="#FFC107" d="M43.6,20.5H42V20H24v8h11.3C33.5,32.4,29.2,36,24,36c-6.6,0-12-5.4-12-12S17.4,12,24,12c3,0,5.7,1.1,7.8,3l5.7-5.7C34.4,6.1,29.5,4,24,4C12.9,4,4,12.9,4,24s8.9,20,20,20c10,0,19-7.3,19-20C43.9,22.6,43.8,21.5,43.6,20.5z"/>
        <path fill="#FF3D00" d="M6.3,14.7l6.6,4.8C14.6,16,18.9,12,24,12c3,0,5.7,1.1,7.8,3l5.7-5.7C34.4,6.1,29.5,4,24,4C16.1,4,9.2,8.3,6.3,14.7z"/>
        <path fill="#4CAF50" d="M24,44c5.2,0,10-2,13.5-5.2l-6.2-5.1C29.3,35.9,26.8,37,24,37c-5.1,0-9.4-3.5-10.9-8.3l-6.5,5.1C9.2,39.7,16.1,44,24,44z"/>
        <path fill="#1976D2" d="M43.6,20.5H42V20H24v8h11.3c-1.3,3.9-4.9,7-9.3,7c-5.1,0-9.4-3.5-10.9-8.3l-6.5,5.1C9.2,39.7,16.1,44,24,44c10,0,19-7.3,19-20C43.9,22.6,43.8,21.5,43.6,20.5z"/>
      </svg>
      <span className={styles.label}>{loading ? "Opening Googleâ€¦" : label}</span>
    </button>
  );
}